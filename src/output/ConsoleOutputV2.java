package output;

public class ConsoleOutputV2 {
    public static void main(String[] args) {
        // 문자열 사이에 개행문자(\n)를 포함하여 출력
        System.out.print("Hello JAVA\n");
        System.out.print("Kim\n" + "Coding\n");
        System.out.print(3 + 6 + "\n");
        System.out.print(2022 + "year\n");

        // 결과적으로 각 출력마다 새로운 줄에서 시작하게 됩니다.
    }
}
